//switch case practice

void main() {
  int num = 5;

  switch (num) {
    case 1:
      print("The given number is 1");
      break;

    case 2:
      print("The give number is 2");
      break;

    case 3:
      print("The given number is 3");
      break;

    default:
      print("Out of range");
      break;
  }
}
